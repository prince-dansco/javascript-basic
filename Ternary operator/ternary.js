// . Write a function that takes a number as input and returns "Even" if it's
//  even and "Odd" if it's odd using the ternary operator.
function nums(num) {
    if (num % 2 == 0) {
        console.log("even");
    } else {
        console.log("odd")
    }
    return num;
}
let result = nums(5);

